import { Component, OnInit, Input} from '@angular/core';
  import { DomSanitizer } from '@angular/platform-browser';
  
  
  @Component({
    selector: 'app-scheduled-maintenance-header',
    templateUrl: './scheduled-maintenance-header.component.html',
    styleUrls: ['./scheduled-maintenance-header.component.scss']
  })
  
  export class ScheduledMaintenanceHeaderComponent implements OnInit {
    
    
  
    constructor(private sanitized: DomSanitizer ) { }
  
    ngOnInit(): void {
    }

    
  
    innerHTMLSanitizer(value?: any){
      return this.sanitized.bypassSecurityTrustHtml(value)
    }
  }

  

  